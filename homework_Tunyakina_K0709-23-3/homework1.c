// №8
// Оптимальный вес груза
// Космический корабль может нести N кг груза (int). 
// Однако, он должен быть равномерно распределен в 4 отсеках. 
// Определите, возможно ли это, и если да, сколько кг будет в каждом отсеке.
void task8() {
    int cargo;
    printf("Введите вес груза: ");
    scanf("%d", &cargo);

    int whole = cargo / 4;
    int rest = cargo % 4;
    if (rest == 0) {
        printf("Груз можно разделить на 4 отсека %d кг весом каждый.\n", whole);
    } else {
        printf("Груз нельзя равномерно распределить.\n");
    }
}
