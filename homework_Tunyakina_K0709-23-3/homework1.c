// №6
// Разделение экипажа
// В космической экспедиции участвуют заранее подготовленные космонавты. Для предстоящего полета подготовлены 
// модульные отсеки для космического корабля. Космическая программа профинансировала изготовление 3-х вариантов модулей, 
// которые вмещают 2-х человек экипажа, 3-х или 4-х человек. По итогу отбора участников, 
// будет выяволено общее число претендентов на экспедицию. Напишите программу, которая определит 
// можно ли разделить число отобранных участников на равные группы, где каждая группа может содержать 2, 3 или 4 человека. 
// На вход программа принимает количество космонавтов и выводит, можно ли их разделить без остатка, 
// а также сколько потребуется полностью заполненных модулей и какого типа: 2-х, 3-х или 4-х местные. 
// Программа должна рассчитать всевозможные варианты комплектаций модулями, 
// исключая непоместившегося космонавта (если такой будет).
void task6() {
    int astronauts;
    printf("Введите количество космонавтов: ");
    scanf("%d", &astronauts);
 
    int solutions = 0;
    for (int i = 0; i <= astronauts / 2; i++) {
        for (int j = 0; j <= (astronauts - 2 * i) / 3; j++) {
            int rest_astronauts = astronauts - 2 * i - 3 * j;
            if (rest_astronauts % 4 == 0) {
                printf("Комплектация: %d 2-х местных, %d 3-х местных, %d 4-х местных модулей\n", i, j, rest_astronauts / 4);
                solutions = 1;
            }
        }
    }
    if (!solutions) {
        printf("Невозможно разместить %d космонавтов(-a).\n", astronauts);
    }
}

